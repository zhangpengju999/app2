package app.entity;

public class TaskStatistic {

	Integer taskId;
	
	String taskName;
	
	double tatalIncome;

	public Integer getTaskId() {
		return taskId;
	}

	public void setTaskId(Integer taskId) {
		this.taskId = taskId;
	}

	public String getTaskName() {
		return taskName;
	}

	public void setTaskName(String taskName) {
		this.taskName = taskName;
	}

	public double getTatalIncome() {
		return tatalIncome;
	}

	public void setTatalIncome(double tatalIncome) {
		this.tatalIncome = tatalIncome;
	}
	
}
